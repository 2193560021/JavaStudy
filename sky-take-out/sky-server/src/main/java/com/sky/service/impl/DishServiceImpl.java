import com.sky.vo.DishVO;
import org.springframework.beans.BeanUtils;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import java.util.List;

@Service
public class DishServiceImpl implements DishService {

//    @Autowired
//    private DishMapper categoryMapper;

    @Autowired
    private DishMapper dishMapper;

    @Autowired
    private DishFlavorMapper dishFlavorMapper;

    @Autowired
    private SetmealMapper setmealMapper;

    @Transactional
    public Integer saveWithFlavor(DishDTO dishDTO) {

        Dish dish = new Dish();
//        //对象属性拷贝
        BeanUtils.copyProperties(dishDTO,dish);
//        //设置账号状态，默认正常状态
        dish.setStatus(StatusConstant.ENABLE);

        Integer insert = dishMapper.insert(dish);

        //获取生成的主键值
        Long dishId = dish.getId();

        List<DishFlavor> flavors = dishDTO.getFlavors();
        if (flavors != null && flavors.size() > 0) {
            flavors.forEach(dishFlavor -> {
                dishFlavor.setDishId(dishId);
            });
            dishFlavorMapper.insertBatch(flavors);
        }
        return insert;
    }

    @Override
    public PageResult page(DishPageQueryDTO dishPageQueryDTO) {
        PageHelper.startPage(dishPageQueryDTO.getPage(), dishPageQueryDTO.getPageSize());
        Page<DishVO> page = dishMapper.pageQuery(dishPageQueryDTO);
        long total = page.getTotal();
        List<DishVO> records = page.getResult();
        PageResult pageResult = new PageResult(total, records);
        return pageResult;
    }

    @Override
    public void setStatus(Integer status, Long id) {
        Dish dish = Dish.builder()
                .status(status)
                .id(id)
                .build();
        dishMapper.update(dish);
    }

    @Override
    public Dish getById(Long id) {
        return dishMapper.getById(id);
    }

    @Override
    public void delete(Long id) {
        if (dishMapper.selectStatus(id) == 0){
            throw new RuntimeException(MessageConstant.DISH_ON_SALE);
        }
        //正常删除
        dishMapper.deleteById(id);
    }

}
